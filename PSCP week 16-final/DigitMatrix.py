    for row in lis:
        num1 += row[0:5]
        oper += row[7:12]
        num2 += row[14:]
    if str(num1) not in cha1.keys() or str(oper) not in cha2.keys() or str(num2) not in cha1.keys():
        print('Error')
    else:
        num1, oper, num2 = str(num1), str(oper), str(num2)
        if cha2[str(oper)] == 'PLUS':
            print(int(cha1[num1]+cha1[num2]))
        elif cha2[str(oper)] == 'TIMES':
            print(int(cha1[num1]*cha1[num2]))
        elif cha2[str(oper)] == 'MINUS':
            print(int(cha1[num1]-cha1[num2]))
        elif cha2[str(oper)] == 'DIVIED':
            if cha1[num2] != 0:
                print(int(ceil(cha1[num1]/cha1[num2])))
            else:
                print('Error')
